-- 01-01 +01:00
-- 06-01 +02:00
-- 12-31 +01:00

COPY (
SELECT TRIM(station_id) as station_id, measurement_time, pm2_5, pm10 FROM gios_observations WHERE measurement_time IN (
'2012-01-01 01:00+01:00',
'2012-06-01 12:00+02:00',
'2012-12-31 23:00+01:00',

'2013-01-01 01:00+01:00',
'2013-06-01 12:00+02:00',
'2013-12-31 23:00+01:00',

'2014-01-01 01:00+01:00',
'2014-06-01 12:00+02:00',
'2014-12-31 23:00+01:00',

'2015-01-01 01:00+01:00',
'2015-06-01 12:00+02:00',
'2015-12-31 23:00+01:00',

'2016-01-01 01:00+01:00',
'2016-06-01 12:00+02:00',
'2016-12-31 23:00+01:00',

'2017-01-01 01:00+01:00',
'2017-06-01 12:00+02:00',
'2017-12-31 23:00+01:00',

'2018-01-01 01:00+01:00',
'2018-06-01 12:00+02:00',
'2018-12-31 23:00+01:00'
)
ORDER BY measurement_time, station_id
) TO '/tmp/gios_observations_imported_values.csv' DELIMITER ',' CSV HEADER;

